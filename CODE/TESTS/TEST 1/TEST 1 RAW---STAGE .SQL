CREATE OR REPLACE VIEW ECOM_DEV_DB.TEST.VW_RAW_CUSTOMERS_VALIDATION AS
SELECT
    R.*,
    CASE WHEN CUSTOMER_ID IS NULL THEN 'FAIL' ELSE 'PASS' END AS CUSTOMER_ID_CHECK,
    CASE WHEN NAME IS NULL OR TRIM(NAME) = '' THEN 'FAIL' ELSE 'PASS' END AS NAME_CHECK,
    CASE WHEN EMAIL IS NULL OR NOT REGEXP_LIKE(EMAIL,'^[^@]+@[^@]+\.[^@]+$') THEN 'FAIL' ELSE 'PASS' END AS EMAIL_CHECK,
    CASE WHEN CREATED_AT IS NULL OR CREATED_AT > CURRENT_DATE() THEN 'FAIL' ELSE 'PASS' END AS CREATED_AT_CHECK
FROM ECOM_DEV_DB.RAW.CUSTOMERS_RAW R;
