-- ============================================================
-- VIEW NAME: VW_CUSTOMER_LIFETIME_VALUE_ANALYTICS
-- PURPOSE: Estimate customer lifetime value (CLV) based on total spend
-- SOURCE: GOLD layer fact (Orders + Payments) and dimension (Customers)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_CUSTOMER_LIFETIME_VALUE_ANALYTICS AS
SELECT
    C.CUSTOMER_ID,
    C.NAME AS CUSTOMER_NAME,
    SUM(P.AMOUNT) AS TOTAL_SPEND,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    ROUND(SUM(P.AMOUNT) / NULLIF(COUNT(DISTINCT O.ORDER_ID),0), 2) AS AVG_ORDER_VALUE,
    ROUND(SUM(P.AMOUNT) / NULLIF(MONTHS_BETWEEN(CURRENT_DATE, MIN(O.ORDER_DATE)),0), 2) AS AVG_MONTHLY_VALUE
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P 
    ON O.ORDER_ID = P.ORDER_ID
JOIN ECOM_DEV_DB.GOLD.DIM_CUSTOMERS C 
    ON O.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY C.CUSTOMER_ID, C.NAME
ORDER BY TOTAL_SPEND DESC;

-- ============================================================
-- VIEW NAME: VW_CUSTOMER_COHORT_ANALYTICS
-- PURPOSE: Analyze customer cohorts based on their first purchase month
-- SOURCE: GOLD layer fact (Orders) and dimension (Customers)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_CUSTOMER_COHORT_ANALYTICS AS
WITH FIRST_ORDER AS (
    SELECT
        CUSTOMER_ID,
        MIN(ORDER_DATE) AS FIRST_ORDER_DATE
    FROM ECOM_DEV_DB.GOLD.FACT_ORDERS
    GROUP BY CUSTOMER_ID
),
COHORTS AS (
    SELECT
        FO.CUSTOMER_ID,
        TO_CHAR(FO.FIRST_ORDER_DATE, 'YYYY-MM') AS COHORT_MONTH,
        TO_CHAR(O.ORDER_DATE, 'YYYY-MM') AS ORDER_MONTH,
        COUNT(DISTINCT O.ORDER_ID) AS ORDERS_IN_MONTH,
        SUM(P.AMOUNT) AS REVENUE_IN_MONTH
    FROM FIRST_ORDER FO
    JOIN ECOM_DEV_DB.GOLD.FACT_ORDERS O 
        ON FO.CUSTOMER_ID = O.CUSTOMER_ID
    JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P 
        ON O.ORDER_ID = P.ORDER_ID
    GROUP BY FO.CUSTOMER_ID, TO_CHAR(FO.FIRST_ORDER_DATE, 'YYYY-MM'), TO_CHAR(O.ORDER_DATE, 'YYYY-MM')
)
SELECT
    COHORT_MONTH,
    ORDER_MONTH,
    COUNT(DISTINCT CUSTOMER_ID) AS ACTIVE_CUSTOMERS,
    SUM(ORDERS_IN_MONTH) AS TOTAL_ORDERS,
    SUM(REVENUE_IN_MONTH) AS TOTAL_REVENUE
FROM COHORTS
GROUP BY COHORT_MONTH, ORDER_MONTH
ORDER BY COHORT_MONTH, ORDER_MONTH;