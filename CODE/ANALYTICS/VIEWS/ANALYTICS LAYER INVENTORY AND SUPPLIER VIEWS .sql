-- ============================================================
-- VIEW NAME: VW_PRODUCT_LIFECYCLE_ANALYTICS
-- PURPOSE: Track product lifecycle stages based on sales activity
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_PRODUCT_LIFECYCLE_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    CASE 
        WHEN SUM(O.QUANTITY) = 0 THEN 'Inactive'
        WHEN SUM(O.QUANTITY) < 100 THEN 'Introduction'
        WHEN SUM(O.QUANTITY) BETWEEN 100 AND 1000 THEN 'Growth'
        WHEN SUM(O.QUANTITY) BETWEEN 1001 AND 5000 THEN 'Maturity'
        ELSE 'Decline'
    END AS LIFECYCLE_STAGE,
    SUM(O.QUANTITY) AS TOTAL_UNITS_SOLD
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.NAME
ORDER BY TOTAL_UNITS_SOLD DESC;