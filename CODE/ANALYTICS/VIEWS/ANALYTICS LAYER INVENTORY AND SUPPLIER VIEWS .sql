-- ============================================================
-- VIEW NAME: VW_DECLINING_PRODUCTS_ANALYTICS
-- PURPOSE: Identify products with declining sales month-over-month
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_DECLINING_PRODUCTS_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    TO_CHAR(O.ORDER_DATE, 'YYYY-MM') AS MONTH,
    SUM(O.QUANTITY) AS UNITS_SOLD,
    LAG(SUM(O.QUANTITY)) OVER (PARTITION BY P.PRODUCT_ID ORDER BY TO_CHAR(O.ORDER_DATE,'YYYY-MM')) AS PREV_MONTH_UNITS,
    CASE 
        WHEN SUM(O.QUANTITY) < LAG(SUM(O.QUANTITY)) OVER (PARTITION BY P.PRODUCT_ID ORDER BY TO_CHAR(O.ORDER_DATE,'YYYY-MM'))
        THEN 'Declining'
        ELSE 'Stable/Increasing'
    END AS SALES_TREND
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.NAME, TO_CHAR(O.ORDER_DATE,'YYYY-MM')
ORDER BY P.PRODUCT_ID, MONTH;

-- ============================================================
-- VIEW NAME: VW_HIGH_RETURN_PRODUCTS_ANALYTICS
-- PURPOSE: Identify products with high return rates
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_HIGH_RETURN_PRODUCTS_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    COUNT(DISTINCT R.RETURN_ID) AS TOTAL_RETURNS,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    ROUND(COUNT(DISTINCT R.RETURN_ID) / NULLIF(COUNT(DISTINCT O.ORDER_ID),0) * 100, 2) AS RETURN_RATE_PERCENT
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
LEFT JOIN ECOM_DEV_DB.GOLD.FACT_RETURNS R ON O.ORDER_ID = R.ORDER_ID
GROUP BY P.PRODUCT_ID, P.NAME
ORDER BY RETURN_RATE_PERCENT DESC;

-- ============================================================
-- VIEW NAME: VW_INVENTORY_AGING_ANALYTICS
-- PURPOSE: Track aging inventory based on last restock date
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_INVENTORY_AGING_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    P.CATEGORY,
    P.STOCK_QUANTITY,
    P.LAST_RESTOCK_DATE,
    (CURRENT_DATE - P.LAST_RESTOCK_DATE) AS DAYS_IN_STOCK
FROM ECOM_DEV_DB.GOLD.DIM_PRODUCTS P
WHERE P.STOCK_QUANTITY > 0
ORDER BY DAYS_IN_STOCK DESC;

