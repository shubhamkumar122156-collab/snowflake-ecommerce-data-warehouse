-- Inventory & Suppliers
-- ============================================================
-- VIEW NAME: VW_OUT_OF_STOCK_PRODUCTS_ANALYTICS
-- PURPOSE: Identify products currently out of stock
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_OUT_OF_STOCK_PRODUCTS_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    P.CATEGORY,
    P.STOCK_QUANTITY
FROM ECOM_DEV_DB.GOLD.DIM_PRODUCTS P
WHERE P.STOCK_QUANTITY = 0
ORDER BY P.CATEGORY, P.NAME;

-- ============================================================
-- VIEW NAME: VW_HIGH_MARGIN_PRODUCTS_ANALYTICS
-- PURPOSE: List products with high profit margins
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_HIGH_MARGIN_PRODUCTS_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    P.CATEGORY,
    P.PRICE,
    P.COST,
    ROUND(((P.PRICE - P.COST) / NULLIF(P.PRICE,0)) * 100, 2) AS MARGIN_PERCENT
FROM ECOM_DEV_DB.GOLD.DIM_PRODUCTS P
WHERE (P.PRICE - P.COST) > 0
ORDER BY MARGIN_PERCENT DESC;

-- ============================================================
-- VIEW NAME: VW_STOCK_TURNOVER_ANALYTICS
-- PURPOSE: Measure stock turnover rate by product
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_STOCK_TURNOVER_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    SUM(O.QUANTITY) AS UNITS_SOLD,
    P.STOCK_QUANTITY,
    ROUND(SUM(O.QUANTITY) / NULLIF(P.STOCK_QUANTITY,0), 2) AS STOCK_TURNOVER_RATIO
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.NAME, P.STOCK_QUANTITY
ORDER BY STOCK_TURNOVER_RATIO DESC;

-- ============================================================
-- VIEW NAME: VW_SUPPLIER_PERFORMANCE_ANALYTICS
-- PURPOSE: Evaluate supplier performance based on delivery timeliness
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_SUPPLIER_PERFORMANCE_ANALYTICS AS
SELECT
    S.SUPPLIER_ID,
    S.NAME AS SUPPLIER_NAME,
    COUNT(DISTINCT SH.SHIPMENT_ID) AS TOTAL_SHIPMENTS,
    ROUND(AVG(SH.DELIVERY_DATE - SH.SHIP_DATE), 2) AS AVG_DELIVERY_DAYS
FROM ECOM_DEV_DB.GOLD.DIM_SUPPLIERS S
JOIN ECOM_DEV_DB.GOLD.FACT_SHIPMENTS SH ON S.SUPPLIER_ID = SH.SUPPLIER_ID
GROUP BY S.SUPPLIER_ID, S.NAME
ORDER BY AVG_DELIVERY_DAYS;

