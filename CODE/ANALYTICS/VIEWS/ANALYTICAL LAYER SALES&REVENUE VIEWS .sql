-- ============================================================
-- VIEW NAME: VW_FASTEST_GROWING_COUNTRIES_ANALYTICS
-- PURPOSE: Identify countries with fastest revenue growth month-over-month
-- SOURCE: GOLD layer fact (Orders + Payments) and dimension (Customers)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_FASTEST_GROWING_COUNTRIES_ANALYTICS AS
SELECT
    C.COUNTRY,
    TO_CHAR(O.ORDER_DATE, 'YYYY-MM') AS MONTH,
    SUM(P.AMOUNT) AS TOTAL_REVENUE,
    LAG(SUM(P.AMOUNT)) OVER (PARTITION BY C.COUNTRY ORDER BY TO_CHAR(O.ORDER_DATE, 'YYYY-MM')) AS PREV_MONTH_REVENUE,
    ROUND(
        (SUM(P.AMOUNT) - LAG(SUM(P.AMOUNT)) OVER (PARTITION BY C.COUNTRY ORDER BY TO_CHAR(O.ORDER_DATE, 'YYYY-MM')))
        / NULLIF(LAG(SUM(P.AMOUNT)) OVER (PARTITION BY C.COUNTRY ORDER BY TO_CHAR(O.ORDER_DATE, 'YYYY-MM')),0) * 100, 2
    ) AS GROWTH_PERCENT
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P
    ON O.ORDER_ID = P.ORDER_ID
JOIN ECOM_DEV_DB.GOLD.DIM_CUSTOMERS C
    ON O.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY C.COUNTRY, TO_CHAR(O.ORDER_DATE, 'YYYY-MM')
ORDER BY GROWTH_PERCENT DESC;

-- ============================================================
-- VIEW NAME: VW_SEASONAL_SALES_ANALYTICS
-- PURPOSE: Analyze sales by season (quarter-based)
-- SOURCE: GOLD layer fact (Orders + Payments)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_SEASONAL_SALES_ANALYTICS AS
SELECT
    CASE 
        WHEN EXTRACT(MONTH FROM O.ORDER_DATE) IN (12,1,2) THEN 'Winter'
        WHEN EXTRACT(MONTH FROM O.ORDER_DATE) IN (3,4,5) THEN 'Spring'
        WHEN EXTRACT(MONTH FROM O.ORDER_DATE) IN (6,7,8) THEN 'Summer'
        WHEN EXTRACT(MONTH FROM O.ORDER_DATE) IN (9,10,11) THEN 'Fall'
    END AS SEASON,
    SUM(P.AMOUNT) AS TOTAL_REVENUE,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P
    ON O.ORDER_ID = P.ORDER_ID
GROUP BY SEASON
ORDER BY TOTAL_REVENUE DESC;

-- ============================================================
-- VIEW NAME: VW_PROMOTION_IMPACT_ANALYTICS
-- PURPOSE: Measure impact of promotions on sales
-- SOURCE: GOLD layer fact (Orders) and dimension (Products)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_PROMOTION_IMPACT_ANALYTICS AS
SELECT
    O.PROMOTION_ID,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    SUM(O.TOTAL_AMOUNT) AS TOTAL_REVENUE,
    ROUND(SUM(O.TOTAL_AMOUNT) / NULLIF(COUNT(DISTINCT O.ORDER_ID),0), 2) AS AVG_ORDER_VALUE
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
WHERE O.PROMOTION_ID IS NOT NULL
GROUP BY O.PROMOTION_ID
ORDER BY TOTAL_REVENUE DESC;