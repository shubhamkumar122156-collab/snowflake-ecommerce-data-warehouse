-- ============================================================
-- VIEW NAME: VW_TOP_PRODUCTS_ANALYTICS
-- PURPOSE: Identify top-selling products based on total revenue
-- SOURCE: GOLD layer fact and dimension tables
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_TOP_PRODUCTS_ANALYTICS AS
SELECT
    P.PRODUCT_ID,
    P.NAME AS PRODUCT_NAME,
    P.CATEGORY,
    SUM(O.TOTAL_AMOUNT) AS TOTAL_REVENUE,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    SUM(O.TOTAL_AMOUNT) / COUNT(DISTINCT O.ORDER_ID) AS AVG_ORDER_VALUE
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_PRODUCTS P
    ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.NAME, P.CATEGORY
ORDER BY TOTAL_REVENUE DESC
LIMIT 10;

-- ============================================================
-- VIEW NAME: VW_MONTHLY_REVENUE_ANALYTICS
-- PURPOSE: Calculate total revenue and order count per month
-- SOURCE: GOLD layer fact tables (Orders + Payments)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_MONTHLY_REVENUE_ANALYTICS AS
SELECT
    TO_CHAR(O.ORDER_DATE, 'YYYY-MM') AS MONTH,
    SUM(P.AMOUNT) AS TOTAL_REVENUE,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    ROUND(SUM(P.AMOUNT) / NULLIF(COUNT(DISTINCT O.ORDER_ID),0), 2) AS AVG_ORDER_VALUE
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P
    ON O.ORDER_ID = P.ORDER_ID
GROUP BY TO_CHAR(O.ORDER_DATE, 'YYYY-MM')
ORDER BY MONTH;

