-- VIEW NAME: VW_PAYMENT_SUCCESS_RATE_ANALYTICS
-- PURPOSE: Calculate payment success vs. failure rate
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_PAYMENT_SUCCESS_RATE_ANALYTICS AS
SELECT
    P.PAYMENT_METHOD,
    COUNT(CASE WHEN UPPER(P.STATUS) = 'SUCCESS' THEN 1 END) AS SUCCESS_COUNT,
    COUNT(CASE WHEN UPPER(P.STATUS) = 'FAILED' THEN 1 END) AS FAILURE_COUNT,
    ROUND(
        COUNT(CASE WHEN UPPER(P.STATUS) = 'SUCCESS' THEN 1 END) 
        / NULLIF(COUNT(P.PAYMENT_ID),0) * 100, 2
    ) AS SUCCESS_RATE_PERCENT
FROM ECOM_DEV_DB.GOLD.FACT_PAYMENTS P
GROUP BY P.PAYMENT_METHOD
ORDER BY SUCCESS_RATE_PERCENT DESC;

-- ============================================================
-- VIEW NAME: VW_CURRENCY_EXCHANGE_IMPACT_ANALYTICS
-- PURPOSE: Measure impact of currency exchange on revenue
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_CURRENCY_EXCHANGE_IMPACT_ANALYTICS AS
SELECT
    P.CURRENCY,
    SUM(P.AMOUNT) AS TOTAL_REVENUE,
    ROUND(SUM(P.AMOUNT * FX.EXCHANGE_RATE), 2) AS REVENUE_IN_BASE_CURRENCY
FROM ECOM_DEV_DB.GOLD.FACT_PAYMENTS P
JOIN ECOM_DEV_DB.GOLD.DIM_EXCHANGE_RATES FX ON P.CURRENCY = FX.CURRENCY
GROUP BY P.CURRENCY
ORDER BY REVENUE_IN_BASE_CURRENCY DESC;

-- ============================================================
-- VIEW NAME: VW_FRAUD_DETECTION_ANALYTICS
-- PURPOSE: Flag suspicious orders (high value or mismatched data)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_FRAUD_DETECTION_ANALYTICS AS
SELECT
    O.ORDER_ID,
    O.CUSTOMER_ID,
    O.TOTAL_AMOUNT,
    P.PAYMENT_METHOD,
    CASE 
        WHEN O.TOTAL_AMOUNT > 10000 THEN 'High Value'
        WHEN O.CUSTOMER_ID IS NULL THEN 'Missing Customer'
        WHEN P.PAYMENT_METHOD IS NULL THEN 'Missing Payment Method'
        ELSE 'Potential Fraud'
    END AS FRAUD_REASON
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
LEFT JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P ON O.ORDER_ID = P.ORDER_ID
WHERE O.TOTAL_AMOUNT > 10000 
   OR O.CUSTOMER_ID IS NULL 
   OR P.PAYMENT_METHOD IS NULL;

