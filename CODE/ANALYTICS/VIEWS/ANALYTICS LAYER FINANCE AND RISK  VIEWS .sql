-- ============================================================
-- VIEW NAME: VW_COMPLIANCE_CHECKS_ANALYTICS
-- PURPOSE: Track compliance issues in orders
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_COMPLIANCE_CHECKS_ANALYTICS AS
SELECT
    O.ORDER_ID,
    O.CUSTOMER_ID,
    O.ORDER_DATE,
    CASE 
        WHEN O.TOTAL_AMOUNT <= 0 THEN 'Invalid Amount'
        WHEN O.CUSTOMER_ID IS NULL THEN 'Missing Customer'
        ELSE 'Compliant'
    END AS COMPLIANCE_STATUS
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O;

-- ============================================================
-- VIEW NAME: VW_CHANNEL_PERFORMANCE_ANALYTICS
-- PURPOSE: Compare performance across sales channels
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_CHANNEL_PERFORMANCE_ANALYTICS AS
SELECT
    O.CHANNEL,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    SUM(P.AMOUNT) AS TOTAL_REVENUE,
    ROUND(SUM(P.AMOUNT) / NULLIF(COUNT(DISTINCT O.ORDER_ID),0), 2) AS AVG_ORDER_VALUE
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P ON O.ORDER_ID = P.ORDER_ID
GROUP BY O.CHANNEL
ORDER BY TOTAL_REVENUE DESC;

-- ============================================================
-- VIEW NAME: VW_CAMPAIGN_CONVERSION_ANALYTICS
-- PURPOSE: Measure conversion rate of marketing campaigns
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_CAMPAIGN_CONVERSION_ANALYTICS AS
SELECT
    M.CAMPAIGN_ID,
    M.CAMPAIGN_NAME,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    COUNT(DISTINCT C.CUSTOMER_ID) AS TOTAL_CUSTOMERS,
    ROUND(COUNT(DISTINCT O.ORDER_ID) / NULLIF(COUNT(DISTINCT C.CUSTOMER_ID),0) * 100, 2) AS CONVERSION_RATE_PERCENT
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_CUSTOMERS C ON O.CUSTOMER_ID = C.CUSTOMER_ID
JOIN ECOM_DEV_DB.GOLD.FACT_MARKETING M ON O.CAMPAIGN_ID = M.CAMPAIGN_ID
GROUP BY M.CAMPAIGN_ID, M.CAMPAIGN_NAME
ORDER BY CONVERSION_RATE_PERCENT DESC;

-- ============================================================
-- VIEW NAME: VW_SUSTAINABILITY_IMPACT_ANALYTICS
-- PURPOSE: Track sustainability impact (e.g., eco-friendly shipments)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_SUSTAINABILITY_IMPACT_ANALYTICS AS
SELECT
    S.CARRIER,
    COUNT(DISTINCT S.SHIPMENT_ID) AS TOTAL_SHIPMENTS,
    SUM(S.CO2_EMISSIONS) AS TOTAL_CO2_EMISSIONS,
    ROUND(AVG(S.CO2_EMISSIONS), 2) AS AVG_CO2_PER_SHIPMENT
FROM ECOM_DEV_DB.GOLD.FACT_SHIPMENTS S
GROUP BY S.CARRIER
ORDER BY TOTAL_CO2_EMISSIONS ASC;
