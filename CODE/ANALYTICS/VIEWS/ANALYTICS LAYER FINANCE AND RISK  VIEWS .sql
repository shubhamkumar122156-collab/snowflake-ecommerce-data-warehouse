-- VIEW NAME: VW_LOGISTICS_COST_ANALYTICS
-- PURPOSE: Track logistics costs by carrier
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_LOGISTICS_COST_ANALYTICS AS
SELECT
    S.CARRIER,
    SUM(S.COST) AS TOTAL_LOGISTICS_COST,
    COUNT(DISTINCT S.SHIPMENT_ID) AS TOTAL_SHIPMENTS,
    ROUND(SUM(S.COST) / NULLIF(COUNT(DISTINCT S.SHIPMENT_ID),0), 2) AS AVG_COST_PER_SHIPMENT
FROM ECOM_DEV_DB.GOLD.FACT_SHIPMENTS S
GROUP BY S.CARRIER
ORDER BY TOTAL_LOGISTICS_COST DESC;

-- ============================================================
-- VIEW NAME: VW_EMPLOYEE_PRODUCTIVITY_ANALYTICS
-- PURPOSE: Measure employee productivity based on orders handled
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_EMPLOYEE_PRODUCTIVITY_ANALYTICS AS
SELECT
    E.EMPLOYEE_ID,
    E.NAME AS EMPLOYEE_NAME,
    COUNT(DISTINCT O.ORDER_ID) AS ORDERS_HANDLED,
    SUM(O.TOTAL_AMOUNT) AS REVENUE_HANDLED
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_EMPLOYEES E ON O.EMPLOYEE_ID = E.EMPLOYEE_ID
GROUP BY E.EMPLOYEE_ID, E.NAME
ORDER BY ORDERS_HANDLED DESC;

-- ============================================================
-- VIEW NAME: VW_COUNTRY_PROFITABILITY_ANALYTICS
-- PURPOSE: Calculate profitability by country
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_COUNTRY_PROFITABILITY_ANALYTICS AS
SELECT
    C.COUNTRY,
    SUM(P.AMOUNT) AS TOTAL_REVENUE,
    SUM(O.COST) AS TOTAL_COST,
    (SUM(P.AMOUNT) - SUM(O.COST)) AS PROFIT
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P ON O.ORDER_ID = P.ORDER_ID
JOIN ECOM_DEV_DB.GOLD.DIM_CUSTOMERS C ON O.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY C.COUNTRY
ORDER BY PROFIT DESC;

-- ============================================================
-- VIEW NAME: VW_CAMPAIGN_ROI_ANALYTICS
-- PURPOSE: Measure ROI of marketing campaigns
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_CAMPAIGN_ROI_ANALYTICS AS
SELECT
    M.CAMPAIGN_ID,
    M.CAMPAIGN_NAME,
    SUM(P.AMOUNT) AS REVENUE,
    SUM(M.SPEND) AS COST,
    ROUND((SUM(P.AMOUNT) - SUM(M.SPEND)) / NULLIF(SUM(M.SPEND),0) * 100, 2) AS ROI_PERCENT
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.FACT_PAYMENTS P ON O.ORDER_ID = P.ORDER_ID
JOIN ECOM_DEV_DB.GOLD.FACT_MARKETING M ON O.CAMPAIGN_ID = M.CAMPAIGN_ID
GROUP BY M.CAMPAIGN_ID, M.CAMPAIGN_NAME
ORDER BY ROI_PERCENT DESC;

-- ============================================================
-- VIEW NAME: VW_OPERATIONAL_EFFICIENCY_ANALYTICS
-- PURPOSE: Track operational efficiency (orders per employee)
-- ============================================================

CREATE OR REPLACE VIEW ECOM_DEV_DB.ANALYTICS.VW_OPERATIONAL_EFFICIENCY_ANALYTICS AS
SELECT
    E.DEPARTMENT,
    COUNT(DISTINCT O.ORDER_ID) AS TOTAL_ORDERS,
    COUNT(DISTINCT E.EMPLOYEE_ID) AS TOTAL_EMPLOYEES,
    ROUND(COUNT(DISTINCT O.ORDER_ID) / NULLIF(COUNT(DISTINCT E.EMPLOYEE_ID),0), 2) AS ORDERS_PER_EMPLOYEE
FROM ECOM_DEV_DB.GOLD.FACT_ORDERS O
JOIN ECOM_DEV_DB.GOLD.DIM_EMPLOYEES E ON O.EMPLOYEE_ID = E.EMPLOYEE_ID
GROUP BY E.DEPARTMENT
ORDER BY ORDERS_PER_EMPLOYEE DESC;

