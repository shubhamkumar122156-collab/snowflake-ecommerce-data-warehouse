    CARRIER VARCHAR,
    STATUS VARCHAR
);

CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.DIM_PAYMENTS (
    DIM_PAYMENT_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PAYMENT_ID INT,
    PAYMENT_METHOD VARCHAR,
    CURRENCY VARCHAR
);

