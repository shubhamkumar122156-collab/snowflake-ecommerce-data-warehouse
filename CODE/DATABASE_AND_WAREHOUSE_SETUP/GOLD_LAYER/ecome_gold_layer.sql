    DIM_PRODUCT_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRODUCT_ID INT,
    NAME VARCHAR,
    CATEGORY VARCHAR,
    PRICE DECIMAL,
    CURRENCY VARCHAR,
    SUPPLIER_ID INT
);

