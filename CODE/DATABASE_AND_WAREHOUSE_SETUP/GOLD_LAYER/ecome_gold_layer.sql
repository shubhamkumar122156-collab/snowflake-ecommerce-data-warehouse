    CONSTRAINT FK_FACTPAYMENTS_DATE FOREIGN KEY (DIM_DATE_KEY) REFERENCES DIM_DATE(DIM_DATE_KEY)
);

CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.FACT_SHIPMENTS (
    FACT_SHIPMENT_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    DIM_SHIPMENT_KEY INT,
    DIM_DATE_KEY INT,
    DELIVERY_DATE DATE,
    STATUS VARCHAR,
    CONSTRAINT FK_FACTSHIPMENTS_SHIPMENT FOREIGN KEY (DIM_SHIPMENT_KEY) REFERENCES DIM_SHIPMENTS(DIM_SHIPMENT_KEY),
    CONSTRAINT FK_FACTSHIPMENTS_DATE FOREIGN KEY (DIM_DATE_KEY) REFERENCES DIM_DATE(DIM_DATE_KEY)
);

CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.FACT_SALES (
    FACT_SALE_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    DIM_PRODUCT_KEY INT,
