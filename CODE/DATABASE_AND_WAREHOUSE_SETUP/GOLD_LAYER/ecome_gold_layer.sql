CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.FACT_ORDERS (
    FACT_ORDER_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    DIM_CUSTOMER_KEY INT,
    DIM_DATE_KEY INT,
    TOTAL_AMOUNT DECIMAL,
    STATUS VARCHAR,
    CONSTRAINT FK_FACTORDERS_CUSTOMER FOREIGN KEY (DIM_CUSTOMER_KEY) REFERENCES DIM_CUSTOMERS(DIM_CUSTOMER_KEY),
    CONSTRAINT FK_FACTORDERS_DATE FOREIGN KEY (DIM_DATE_KEY) REFERENCES DIM_DATE(DIM_DATE_KEY)
);

CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.FACT_ORDER_PRODUCTS (
    FACT_ORDER_PRODUCT_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    FACT_ORDER_KEY INT,
    DIM_PRODUCT_KEY INT,
    QUANTITY INT,
    LINE_AMOUNT DECIMAL,
    CONSTRAINT FK_FACTORDERPRODUCT_ORDER FOREIGN KEY (FACT_ORDER_KEY) REFERENCES FACT_ORDERS(FACT_ORDER_KEY),
    CONSTRAINT FK_FACTORDERPRODUCT_PRODUCT FOREIGN KEY (DIM_PRODUCT_KEY) REFERENCES DIM_PRODUCTS(DIM_PRODUCT_KEY)
);

CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.FACT_PAYMENTS (
    FACT_PAYMENT_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
