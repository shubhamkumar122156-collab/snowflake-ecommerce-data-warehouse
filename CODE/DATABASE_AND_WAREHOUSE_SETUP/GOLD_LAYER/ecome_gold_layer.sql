    NAME VARCHAR,
    ROLE VARCHAR,
    DEPARTMENT VARCHAR,
    COUNTRY VARCHAR
);

CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.DIM_SHIPMENTS (
