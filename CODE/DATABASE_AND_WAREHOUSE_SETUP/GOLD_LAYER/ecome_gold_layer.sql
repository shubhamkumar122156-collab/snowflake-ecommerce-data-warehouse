CREATE OR REPLACE TABLE ECOM_DEV_DB.GOLD.DIM_CUSTOMERS (
    DIM_CUSTOMER_KEY INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID INT,
    NAME VARCHAR,
    EMAIL VARCHAR,
    COUNTRY VARCHAR
);

