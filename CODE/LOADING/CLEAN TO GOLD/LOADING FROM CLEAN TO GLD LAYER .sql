-- =====================================================================
-- FACT_SHIPMENTS
-- =====================================================================
INSERT INTO ECOM_DEV_DB.GOLD.FACT_SHIPMENTS
(
    SHIPMENT_ID,
    SHIP_DATE_KEY,
    DELIVERY_DATE_KEY,
    CARRIER,
    STATUS,
    SHIP_DATE,
    DELIVERY_DATE,
    DELIVERY_DAYS
)
SELECT
    S.SHIPMENT_ID,
    SD.DATE_KEY AS SHIP_DATE_KEY,
    DD.DATE_KEY AS DELIVERY_DATE_KEY,
    S.CARRIER,
    S.STATUS,
    S.SHIP_DATE,
    S.DELIVERY_DATE,
    DATEDIFF(DAY, S.SHIP_DATE, S.DELIVERY_DATE) AS DELIVERY_DAYS
FROM ECOM_DEV_DB.CLEAN.SHIPMENTS_CLEAN S
INNER JOIN ECOM_DEV_DB.GOLD.DIM_DATE SD
    ON S.SHIP_DATE = SD.FULL_DATE
INNER JOIN ECOM_DEV_DB.GOLD.DIM_DATE DD
    ON S.DELIVERY_DATE = DD.FULL_DATE;