WHERE EMPLOYEE_ID IS NULL
   OR NAME IS NULL OR TRIM(NAME) = ''
   OR ROLE IS NULL OR TRIM(ROLE) = ''
   OR DEPARTMENT IS NULL OR TRIM(DEPARTMENT) = ''
   OR COUNTRY IS NULL OR TRIM(COUNTRY) = ''
   OR HIRED_DATE IS NULL
   OR HIRED_DATE > CURRENT_DATE();

-- STEP 1 : STANDARDIZATION + VALIDATION
CREATE OR REPLACE TEMP TABLE TEMP_EMPLOYEES_STEP1 AS
SELECT
    EMPLOYEE_ID,
    REGEXP_REPLACE(UPPER(TRIM(NAME)),'\\s+',' ') AS NAME,
    UPPER(TRIM(ROLE)) AS ROLE,
    UPPER(TRIM(DEPARTMENT)) AS DEPARTMENT,
    UPPER(TRIM(COUNTRY)) AS COUNTRY,
    HIRED_DATE
FROM ECOM_DEV_DB.STAGE.STAGE_EMPLOYEES
WHERE EMPLOYEE_ID IS NOT NULL
  AND NAME IS NOT NULL AND TRIM(NAME) <> ''
  AND ROLE IS NOT NULL AND TRIM(ROLE) <> ''
  AND DEPARTMENT IS NOT NULL AND TRIM(DEPARTMENT) <> ''
  AND COUNTRY IS NOT NULL AND TRIM(COUNTRY) <> ''
  AND HIRED_DATE IS NOT NULL
  AND HIRED_DATE <= CURRENT_DATE();

-- STEP 2A : LOG EXACT DUPLICATE RECORDS TO DQ
INSERT INTO ECOM_DEV_DB.DQ.DQ_ERROR_LOG
(
    PIPELINE_NAME,
    LAYER_NAME,
    TABLE_NAME,
    BUSINESS_KEY,
    ERROR_TYPE,
    ERROR_MESSAGE,
    LOAD_TIMESTAMP,
    RAW_RECORD
)
SELECT
    'EMPLOYEE_PIPELINE',
    'STAGE_TO_CLEAN',
    'STAGE_EMPLOYEES',
    TO_VARCHAR(EMPLOYEE_ID),
    'DUPLICATE_EMPLOYEE_RECORD',
    'EXACT DUPLICATE EMPLOYEE RECORD REMOVED DURING CLEANING',
    CURRENT_TIMESTAMP(),
    OBJECT_CONSTRUCT(*)
FROM (
    SELECT *,
           ROW_NUMBER() OVER (
               PARTITION BY EMPLOYEE_ID, NAME, ROLE, DEPARTMENT, COUNTRY, HIRED_DATE
               ORDER BY EMPLOYEE_ID
           ) AS RN
    FROM TEMP_EMPLOYEES_STEP1
) X
WHERE RN > 1;

-- STEP 2 : REMOVE EXACT DUPLICATES
CREATE OR REPLACE TEMP TABLE TEMP_EMPLOYEES_STEP2 AS
SELECT DISTINCT EMPLOYEE_ID, NAME, ROLE, DEPARTMENT, COUNTRY, HIRED_DATE
FROM TEMP_EMPLOYEES_STEP1;

-- STEP 3 : FIND CONFLICTING EMPLOYEE IDS
CREATE OR REPLACE TEMP TABLE TEMP_BAD_EMPLOYEE_IDS AS
SELECT EMPLOYEE_ID
FROM TEMP_EMPLOYEES_STEP2
GROUP BY EMPLOYEE_ID
HAVING COUNT(DISTINCT CONCAT_WS('|',NAME,ROLE,DEPARTMENT,COUNTRY,HIRED_DATE)) > 1;

-- STEP 3A : LOG CONFLICTING EMPLOYEE IDS TO DQ
INSERT INTO ECOM_DEV_DB.DQ.DQ_ERROR_LOG
(
    PIPELINE_NAME,
    LAYER_NAME,
    TABLE_NAME,
    BUSINESS_KEY,
    ERROR_TYPE,
    ERROR_MESSAGE,
    LOAD_TIMESTAMP,
    RAW_RECORD
)
SELECT
    'EMPLOYEE_PIPELINE',
    'STAGE_TO_CLEAN',
    'STAGE_EMPLOYEES',
    TO_VARCHAR(A.EMPLOYEE_ID),
    'CONFLICTING_EMPLOYEE_RECORD',
    'EMPLOYEE_ID HAS MULTIPLE DIFFERENT NAME / ROLE / DEPARTMENT / COUNTRY / HIRED_DATE VALUES',
    CURRENT_TIMESTAMP(),
    OBJECT_CONSTRUCT(*)
FROM TEMP_EMPLOYEES_STEP2 A
INNER JOIN TEMP_BAD_EMPLOYEE_IDS B ON A.EMPLOYEE_ID = B.EMPLOYEE_ID;

-- STEP 4 : REMOVE CONFLICTING EMPLOYEE IDS
CREATE OR REPLACE TEMP TABLE TEMP_EMPLOYEES_FINAL AS
SELECT *
FROM TEMP_EMPLOYEES_STEP2 A
WHERE NOT EXISTS (
    SELECT 1
    FROM TEMP_BAD_EMPLOYEE_IDS B
    WHERE A.EMPLOYEE_ID = B.EMPLOYEE_ID
);

