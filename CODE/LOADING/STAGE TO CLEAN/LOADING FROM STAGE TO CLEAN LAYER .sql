-- STEP 1A : INVALID EMPLOYEE RECORDS TO DQ
INSERT INTO ECOM_DEV_DB.DQ.DQ_ERROR_LOG
(
    PIPELINE_NAME,
    LAYER_NAME,
    TABLE_NAME,
    BUSINESS_KEY,
    ERROR_TYPE,
    ERROR_MESSAGE,
    LOAD_TIMESTAMP,
    RAW_RECORD
)
SELECT
    'EMPLOYEE_PIPELINE',
    'STAGE_TO_CLEAN',
    'STAGE_EMPLOYEES',
    COALESCE(TO_VARCHAR(EMPLOYEE_ID), 'UNKNOWN'),
    CASE
        WHEN EMPLOYEE_ID IS NULL THEN 'NULL_EMPLOYEE_ID'
        WHEN NAME IS NULL OR TRIM(NAME) = '' THEN 'INVALID_EMPLOYEE_NAME'
        WHEN ROLE IS NULL OR TRIM(ROLE) = '' THEN 'INVALID_ROLE'
        WHEN DEPARTMENT IS NULL OR TRIM(DEPARTMENT) = '' THEN 'INVALID_DEPARTMENT'
        WHEN COUNTRY IS NULL OR TRIM(COUNTRY) = '' THEN 'INVALID_COUNTRY'
        WHEN HIRED_DATE IS NULL THEN 'NULL_HIRED_DATE'
        WHEN HIRED_DATE > CURRENT_DATE() THEN 'FUTURE_HIRED_DATE'
    END AS ERROR_TYPE,
    CASE
        WHEN EMPLOYEE_ID IS NULL THEN 'EMPLOYEE ID IS NULL'
        WHEN NAME IS NULL OR TRIM(NAME) = '' THEN 'EMPLOYEE NAME IS NULL OR BLANK'
        WHEN ROLE IS NULL OR TRIM(ROLE) = '' THEN 'ROLE IS NULL OR BLANK'
        WHEN DEPARTMENT IS NULL OR TRIM(DEPARTMENT) = '' THEN 'DEPARTMENT IS NULL OR BLANK'
        WHEN COUNTRY IS NULL OR TRIM(COUNTRY) = '' THEN 'COUNTRY IS NULL OR BLANK'
        WHEN HIRED_DATE IS NULL THEN 'HIRED DATE IS NULL'
        WHEN HIRED_DATE > CURRENT_DATE() THEN 'HIRED DATE CANNOT BE FUTURE DATE'
    END AS ERROR_MESSAGE,
    CURRENT_TIMESTAMP(),
    OBJECT_CONSTRUCT(*)
